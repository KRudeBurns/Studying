public class Dog {
    // put your variables at the top
    String name;

    //all other methods come next
    public void bark() {
        System.out.println(name + " says Ruff!");
    }

    public void eat() {
        System.out.println(name + " likes to eat whole foods");
    }

    public void chaseCat() {
        System.out.println(name + " is chasing the cat around the yard!");
    }
}
